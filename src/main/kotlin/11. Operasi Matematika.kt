fun main() {
    // Augmented Assignment
    var total = 10
    val barang1 = 200
    total += barang1
    println(total)

    val barang2 = 200
    total *= barang2
    println(total)

    val barang3 = 200
    total /= barang3
    println(total)

    val barang4 = 200
    total -= barang4
    println(total)

    val barang5 = 200
    total %= barang5
    println(total)

    // Unary Operator
    total++
    total--
    println(total)

    total = -20 // -20
    total = 20 // +20
    println(total)

    val nilai = true
    println(!nilai) // Boolean Kebalikan / Negasi

}

