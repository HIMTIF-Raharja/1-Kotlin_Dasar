fun main() {
    val examValue = 80
// Dibacanya jika examValue lebih dari 80 maka akan menghasilkan Good Job

    if(examValue > 90){
        println("Cool")
    }
    else if (examValue > 75) {
        println("Good Job")
    }
    else if (examValue > 60) {
        println("Not Bad")
    }
    else{
        println("Anda kurang pintar")
    }

}