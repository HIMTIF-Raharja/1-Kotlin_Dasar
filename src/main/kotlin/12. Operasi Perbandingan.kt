fun main() {
    val nilai1 : Int = 100
    val nilai2 : Int = 200

    val result1 : Boolean = nilai1 < nilai2
    println(result1)

    val result2 = nilai1 > nilai2
    println(result2)

    val result3 = nilai1 <= nilai2
    println(result3)

    val result4 = nilai1 >= nilai2
    println(result4)

    val result5 = nilai1 == nilai2
    println(result5)

    val result6 = nilai1 != nilai2
    println(result6)

    // Bisa juga menggunakan Text untuk perbandingan
    println("a" < "b") // Hasilnya True karena abjad a lebih dahulu dari b
    println("a" > "b") // Bisa digunakan apapun Operator Perbandingan di String
}

